package ru.job4j.basics.branching;

/*
  Ниже представлен метод triangleType(int a, int b), который принимает 2 целых
  положительных числа - значения двух углов в треугольнике.

  Задание: Написать код, который определяет тип этого треугольника -
  остроугольный, тупоугольный, прямоугольный, равнобедренный, равносторонний
  (возможно, что треугольник относится сразу к нескольким типам). Вывести в
  консоль тип треугольника, определенный по входящим параметрам. Если
  треугольник имеет 2 или 3 типа - вывести их построчно (вывод типов соблюдать в
  порядке, в котором они перечислены ниже). В случае, если треугольник с
  входящими параметрами не может существовать - вывести "Ошибка".

  Например, для чисел 45, 90 вывод будет:

  Прямоугольный

  Равнобедренный

  Виды треугольников и их особенности:

  Остроугольный - треугольник, все углы которого острые (то есть градусная мера
  каждого угла меньше 90 градусов).

  Прямоугольный - треугольник, у которого один угол прямой (то есть 90 градусов).

  Тупоугольный - треугольник, у которого один угол тупой (то есть больше 90
  градусов).

  Равнобедренный - треугольник, у которого 2 стороны равны.

  Равнобедренный прямоугольный треугольник - прямоугольный треугольник, катеты
  которого равны (то есть углы катетов у основания тоже равны - по 45 градусов).

  Равносторонний - треугольник, у которого все 3 стороны равны (все углы по 60
  градусов).

  Равносторонний треугольник является частным случаем равнобедренного
  треугольника.
*/
public class Task57 {
    public static void triangleType(int a, int b) {
        int c = 180 - a - b;

        // Проверка существования треугольника
        if (a <= 0 || b <= 0 || c <= 0) {
            System.out.println("Ошибка");
            return;
        }

        String result = "";

        // Тип по углам треугольника
        if (a < 90 && b < 90 && c < 90) {
            result += "Остроугольный ";
        }

        if (a == 90 || b == 90 || c == 90) {
            result += "Прямоугольный ";
        }

        if (a > 90 || b > 90 || c > 90) {
            result += "Тупоугольный ";
        }

        // Тип по сторонам треугольника (через углы)
        if (a == b || a == c || b == c) {
            result += "Равнобедренный ";
        }

        if (a == 60 && b == 60 && c == 60) {
            result += "Равносторонний ";
        }

        System.out.println(result.trim());
    }

    public static void main(String[] args) {
        triangleType(45, 90);
    }
}
