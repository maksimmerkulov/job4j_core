package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task29} представляет утилиту для вычисления суммы цифр
 * положительного трехзначного числа.
 *
 * <p>Содержит метод {@link #numSum(int)}, который принимает
 * число от {@code 100} до {@code 999}, вычисляет сумму сотен, десятков и единиц
 * и выводит результат на консоль.</p>
 *
 * <p>Примеры использования: вызов метода {@link #numSum(int)}
 * с числом {@code 325}, результат выводится на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task29 {

    /**
     * Вычисляет сумму цифр переданного трехзначного числа и выводит результат.
     *
     * <p>Используются только операции деления, остатка
     * и базовые арифметические операции.</p>
     *
     * @param number положительное трехзначное число
     *               (от {@code 100} до {@code 999})
     */
    public static void numSum(int number) {
        int num1, num2, num3;
        num1 = number / 100;
        num2 = (number / 10) % 10;
        num3 = number % 10;
        System.out.println(num1 + num2 + num3);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрация работы метода {@link #numSum(int)}.</p>
     * <p>Создает пример числа и выводит сумму его цифр на консоль.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        numSum(325);
    }
}
