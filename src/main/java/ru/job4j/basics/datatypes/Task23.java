package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task23} представляет утилиту
 * для разложения трехзначного числа на цифры и вывода их в обратном порядке.
 *
 * <p>Содержит метод, который выделяет цифры сотен, десятков и единиц
 * и выводит их на консоль в формате: единицы, десятки, сотни.</p>
 *
 * <p>Примеры использования: передача трехзначного числа
 * в метод {@code separate(int)} и вывод результата на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task23 {

    /**
     * Разделяет трехзначное число на цифры и выводит их в обратном порядке:
     * единицы, десятки, сотни.
     *
     * <p>Метод использует операции деления и остатка от деления
     * для получения каждой цифры числа.</p>
     *
     * @param number трехзначное число
     */
    public static void separate(int number) {
        int num1, num2, num3;
        num1 = number / 100;
        num2 = (number / 10) % 10;
        num3 = number % 10;
        System.out.println(num3 + " " + num2 + " " + num1);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрирует работу метода {@link #separate(int)}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        separate(205);
    }
}
