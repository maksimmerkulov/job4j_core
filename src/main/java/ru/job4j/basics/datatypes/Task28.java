package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task28} представляет утилиту для вычисления суммы цифр
 * положительного двузначного числа.
 *
 * <p>Содержит метод {@link #numSum(int)}, который принимает
 * целое число от {@code 10} до {@code 99}, вычисляет сумму десятков и единиц
 * и выводит результат на консоль.</p>
 *
 * <p>Примеры использования: вызов метода {@link #numSum(int)}
 * с числом {@code 35}, результат выводится на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task28 {

    /**
     * Вычисляет сумму цифр переданного двузначного числа и выводит результат.
     *
     * <p>Используются только операции деления, остатка
     * и базовые арифметические операции.</p>
     *
     * @param number положительное двузначное число
     *               (от {@code 10} до {@code 99})
     */
    public static void numSum(int number) {
        int num1, num2;
        num1 = number / 10;
        num2 = number % 10;
        System.out.println(num1 + num2);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрация работы метода {@link #numSum(int)}.</p>
     * <p>Создает пример числа и выводит сумму его цифр на консоль.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        numSum(35);
    }
}
