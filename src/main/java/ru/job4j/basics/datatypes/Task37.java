package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task37} выполняет сдвиг цифр трехзначного числа влево.
 *
 * <p>Содержит метод, который удаляет первую цифру числа,
 * переносит оставшиеся цифры влево и добавляет ноль в конец результата.</p>
 *
 * <p>Примеры использования: вызов метода {@code leftShift(int)}
 * для преобразования трехзначного числа и вывода результата на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task37 {

    /**
     * Выполняет сдвиг цифр числа влево.
     *
     * <p>Удаляет первую цифру трехзначного числа
     * с помощью операции остатка от деления на {@code 100},
     * затем умножает результат на {@code 10}
     * для добавления нуля в конец числа.</p>
     *
     * @param number трехзначное целое положительное число.
     */
    public static void leftShift(int number) {
        int num;
        num = number % 100;
        System.out.println(num * 10);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрирует работу метода {@code leftShift(int)}
     * на примере числа {@code 123}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        leftShift(123);
    }
}
