package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task27} представляет собой инструмент
 * для трансформации трехзначного числа.
 *
 * <p>Содержит метод {@link #transform(int)}, который принимает
 * трехзначное число, переставляет его цифры в обратном порядке,
 * уменьшает полученное значение на {@code 20}
 * и выводит результат на консоль.</p>
 *
 * <p>Примеры использования: преобразование числа {@code 327} в {@code 703},
 * числа {@code 451} в {@code 124}, числа {@code 100} в {@code 1}.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task27 {

    /**
     * Трансформирует входящее трехзначное число.
     *
     * <p>Метод разбивает число на сотни, десятки и единицы,
     * переставляет цифры в обратном порядке, уменьшает результат на {@code 20}
     * и выводит его на консоль.</p>
     *
     * @param number входящее трехзначное число
     */
    public static void transform(int number) {
        int num1, num2, num3;
        num1 = number / 100;
        num2 = (number / 10) % 10;
        num3 = number % 10;
        System.out.println(num3 * 100 + num2 * 10 + num1 - 20);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрация работы метода {@link #transform(int)}
     * с примером числа {@code 327}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        transform(327);
    }
}
