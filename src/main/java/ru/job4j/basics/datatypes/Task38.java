package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task38} представляет утилитарный класс.
 *
 * <p>Содержит логику линейного сдвига четырехзначного положительного числа
 * на одну позицию влево с удалением первой цифры
 * и добавлением нуля в разряд единиц.</p>
 *
 * <p>Примеры использования: вызов метода {@link #leftShift(int)}
 * для обработки входного числа и вывода результата на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task38 {

    /**
     * Выполняет линейный сдвиг числа влево.
     *
     * <p>Первая цифра числа удаляется,
     * оставшиеся цифры сдвигаются на одну позицию влево,
     * а в разряд единиц записывается ноль.</p>
     *
     * @param number четырехзначное положительное число
     */
    public static void leftShift(int number) {
        int num;
        num = number % 1000;
        System.out.println(num * 10);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрирует работу метода {@link #leftShift(int)}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        leftShift(1234);
    }
}
