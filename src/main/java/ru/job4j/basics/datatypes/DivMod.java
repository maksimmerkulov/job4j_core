package ru.job4j.basics.datatypes;

/**
 * Класс {@code DivMod} демонстрирует деление
 * и получение остатка от деления целого числа.
 *
 * <p>Содержит единственный метод {@code main}, который вычисляет
 * результат целочисленного деления и остаток от деления числа
 * на заданное значение и выводит их на консоль.</p>
 *
 * <p>Примеры использования: запуск программы выводит результат
 * деления числа {@code 11} на {@code 3} и остаток от деления
 * на отдельных строках.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class DivMod {

    /**
     * Точка входа в программу.
     *
     * <p>Вычисляет результат деления и остаток от деления числа
     * {@code 11} на {@code 3} и выводит результаты на консоль.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        int num = 11;
        int div = num / 3;
        int mod = num % 3;
        System.out.println(div);
        System.out.println(mod);
    }
}
