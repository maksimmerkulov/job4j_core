package ru.job4j.basics.datatypes;

/**
 * Класс {@code Task42} представляет {@code "сдвиг числа вправо по кругу"}.
 *
 * <p>Содержит метод для выполнения {@code "сдвига числа вправо по кругу"}
 * для четырехзначного числа: последняя цифра числа перемещается на место
 * первой, остальные цифры смещаются вправо на одну позицию.</p>
 *
 * <p>Примеры использования: создание объектов класса не требуется,
 * метод {@link #rightCircleShift(int)} вызывается напрямую
 * и результат выводится на консоль.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task42 {

    /**
     * Выполняет {@code "сдвиг числа вправо по кругу"}
     * для четырехзначного числа.
     *
     * <p>Последняя цифра числа становится первой, остальные цифры смещаются
     * вправо на одну позицию. Результат выводится на консоль.</p>
     *
     * @param number четырехзначное целое положительное число
     */
    public static void rightCircleShift(int number) {
        int firstThree, last;
        firstThree = number / 10;
        last = number % 10;
        System.out.println(last * 1000 + firstThree);
    }

    /**
     * Точка входа в программу.
     *
     * <p>Демонстрация работы метода {@link #rightCircleShift(int)}.</p>
     * <p>Для примера выполняется сдвиг числа {@code 1234}.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        rightCircleShift(1234);
    }
}
