package ru.job4j.basics.console;

/**
 * Класс {@code Task20} демонстрирует вывод двух переменных,
 * их суммы и произведения на консоль.
 *
 * <p>Содержит статический метод {@code print}, который выполняет вычисления
 * и выводит результаты на консоль.</p>
 *
 * <p>Примеры использования: запуск программы вызывает
 * метод {@code print} с аргументами и выводит на консоль значения переменных,
 * их сумму и произведение.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task20 {

    /**
     * Выводит значения переменных {@code x} и {@code y},
     * их сумму и произведение на консоль.
     *
     * @param x первая переменная
     * @param y вторая переменная
     */
    public static void print(int x, int y) {
        System.out.println("Переменные: " + x + " " + y);
        System.out.println("Сумма: " + (x + y));
        System.out.println("Произведение: " + (x * y));
    }

    /**
     * Точка входа в программу.
     *
     * <p>Вызов метода {@code print} с аргументами 3 и 5
     * для демонстрации работы вычислений и вывода на консоль.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        print(3, 5);
    }
}
