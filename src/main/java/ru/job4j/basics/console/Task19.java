package ru.job4j.basics.console;

/**
 * Класс {@code Task19} демонстрирует вывод значения переменной
 * и ее квадрата на консоль.
 *
 * <p>Содержит единственный метод {@code main}, который выполняет
 * базовые вычисления и вывод на консоль.</p>
 *
 * <p>Примеры использования: запуск программы выводит строку
 * со значением переменной {@code x} и значением ее квадрата.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Task19 {

    /**
     * Точка входа в программу.
     *
     * <p>Выводит на консоль значение переменной {@code x}
     * и значение ее квадрата.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        int x = 9;
        System.out.println("Значение x равно " + x);
        System.out.println("Значение x^2 равно " + x * x);
    }
}
