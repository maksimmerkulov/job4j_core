package ru.job4j.basics.console;

/**
 * Класс {@code Praise} выводит мотивационное сообщение на консоль.
 *
 * <p>Содержит единственный метод {@code main}, который демонстрирует
 * базовый вывод текста на консоль.</p>
 *
 * <p>Примеры использования: запуск программы выводит строку
 * "Сегодня я молодец!" на стандартный вывод.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Praise {

    /**
     * Точка входа в программу.
     *
     * <p>Выводит на консоль мотивационное сообщение.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        System.out.println("Сегодня я молодец!");
    }
}
