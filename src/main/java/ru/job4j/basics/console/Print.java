package ru.job4j.basics.console;

/**
 * Класс {@code Print} выводит текст без перевода строки на консоль.
 *
 * <p>Содержит единственный метод {@code main}, который демонстрирует
 * использование метода {@code System.out.print}.</p>
 *
 * <p>Примеры использования: запуск программы выводит строку
 * "Сегодня я молодец!" без переноса строки.</p>
 *
 * @author Maksim Merkulov
 * @version 1.0
 */
public class Print {

    /**
     * Точка входа в программу.
     *
     * <p>Выводит текст "Сегодня я молодец!" без перевода строки на консоль.</p>
     *
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        System.out.print("Сегодня я молодец!");
    }
}
